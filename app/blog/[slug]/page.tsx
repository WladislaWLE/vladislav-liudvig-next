function renderContent(md: string) {
  const lines = md.trim().split("\n");
  const elements: React.ReactNode[] = [];
  let key = 0;

  let tableRows: string[] = [];
  let inTable = false;

  const flushTable = () => {
    if (tableRows.length < 2) { tableRows = []; inTable = false; return; }
    const [header, , ...body] = tableRows;
    const heads = (header as string).split("|").map((c: string) => c.trim()).filter(Boolean);
    elements.push(
      <div key={key++} className="overflow-x-auto my-6">
        <table className="w-full text-sm border-collapse">
          <thead>
            <tr>
              {heads.map((h: string, i: number) => (
                <th key={i} className="text-left px-4 py-3 bg-white/5 border border-white/10 text-white/80 font-semibold">{h}</th>
              ))}
            </tr>
          </thead>
          <tbody>
            {body.map((row: string, ri: number) => {
              const cells = row.split("|").map((c: string) => c.trim()).filter(Boolean);
              return (
                <tr key={ri} className="border-b border-white/5 hover:bg-white/3 transition-colors">
                  {cells.map((cell: string, ci: number) => (
                    <td key={ci} className="px-4 py-3 text-white/60 border border-white/8"
                      dangerouslySetInnerHTML={{ __html: cell.replace(/\*\*(.+?)\*\*/g, "<strong class='text-white/90'>$1</strong>") }}
                    />
                  ))}
                </tr>
              );
            })}
          </tbody>
        </table>
      </div>
    );
    tableRows = [];
    inTable = false;
  };

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];

    // Table detection
    if (line.startsWith("|")) {
      inTable = true;
      tableRows.push(line);
      continue;
    }
    if (inTable) {
      flushTable();
    }

    if (!line.trim()) {
      elements.push(<div key={key++} className="h-3" />);
      continue;
    }

    if (line.startsWith("## ")) {
      elements.push(
        <h2 key={key++} className="font-display font-bold text-2xl text-white mt-10 mb-4 first:mt-0">
          {line.replace("## ", "")}
        </h2>
      );
    } else if (line.startsWith("**") && line.endsWith("**")) {
      elements.push(
        <p key={key++} className="font-semibold text-white/90 mt-4 mb-1">
          {line.replace(/\*\*/g, "")}
        </p>
      );
    } else if (line.startsWith("- ")) {
      // Collect consecutive list items into a <ul>
      const listItems: string[] = [line];
      while (i + 1 < lines.length && lines[i + 1].startsWith("- ")) {
        i++;
        listItems.push(lines[i]);
      }
      elements.push(
        <ul key={key++} className="space-y-1.5 my-2">
          {listItems.map((item, li) => (
            <li key={li} className="flex gap-2 text-white/70 leading-relaxed ml-4">
              <span className="text-accent mt-1.5 shrink-0">•</span>
              <span dangerouslySetInnerHTML={{ __html: item.replace("- ", "").replace(/\*\*(.+?)\*\*/g, "<strong class='text-white/90'>$1</strong>") }} />
            </li>
          ))}
        </ul>
      );
    } else {
      elements.push(
        <p key={key++} className="text-white/65 leading-relaxed"
          dangerouslySetInnerHTML={{ __html: line.replace(/\*\*(.+?)\*\*/g, "<strong class='text-white/90'>$1</strong>") }}
        />
      );
    }
  }

  if (inTable) flushTable();

  return elements;
}

